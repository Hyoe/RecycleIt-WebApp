      <ul>
        <li>Recycling and composting diverted nearly 70 million tons of material away from landfills and incinerators in 2000, up from 34 million tons in 1990-doubling in just 10 years.</li>
        <li>Every ton of paper that is recycled saves 17 trees.</li>
        <li>The energy we save when we recycle one glass bottle is enough to light a light bulb for four hours.</li>
        <li>Recycling benefits the air and water by creating a net reduction in ten major categories of air pollutants and eight major categories of water pollutants.</li>
        <li>In the U.S., processing minerals contributes almost half of all reported toxic emissions from industry, sending 1.5 million tons of pollution into the air and water each year. Recycling can significantly reduce these emissions.</li>
        <li>It is important to reduce our reliance on foreign oil. Recycling helps us do that by saving energy.</li>
        <li>Manufacturing with recycled materials, with very few exceptions, saves energy and water and produces less air and water pollution than manufacturing with virgin materials.</li>
        <li>It takes 95% less energy to recycle aluminum than it does to make it from raw materials. Making recycled steel saves 60%, recycled newspaper 40%, recycled plastics 70%, and recycled glass 40%. These savings far outweigh the energy created as by-products of incineration and landfilling.</li>
        <li>In 2000, recycling resulted in an annual energy savings equal to the amount of energy used in 6 million homes (over 660 trillion BTUs). In 2005, recycling is conservatively projected to save the amount of energy used in 9 million homes (900 trillion BTUs).</li>
        <li>A national recycling rate of 30% reduces greenhouse gas emissions as much as removing nearly 25 million cars from the road.</li>
        <li>Recycling conserves natural resources, such as timber, water, and minerals.</li>
        <li>Every bit of recycling makes a difference. For example, one year of recycling on just one college campus, Stanford University, saved the equivalent of 33,913 trees and the need for 636 tons of iron ore, coal, and limestone.</li>
        <li>Recycled paper supplies more than 37% of the raw materials used to make new paper products in the U.S. Without recycling, this material would come from trees. Every ton of newsprint or mixed paper recycled is the equivalent of 12 trees. Every ton of office paper recycled is the equivalent of 24 trees.</li>
        <li>When one ton of steel is recycled, 2,500 pounds of iron ore, 1,400 pounds of coal and 120 pounds of limestone are conserved.</li>
        <li>Brutal wars over natural resources, including timber and minerals, have killed or displaced more than 20 million people and are raising at least $12 billion a year for rebels, warlords, and repressive governments. Recycling eases the demand for the resources.</li>
        <li>Mining is the world's most deadly occupation. On average, 40 mine workers are killed on the job each day, and many more are injured. Recycling reduces the need for mining.</li>
        <li>Tree farms and reclaimed mines are not ecologically equivalent to natural forests and ecosystems.</li>
        <li>Recycling prevents habitat destruction, loss of biodiversity, and soil erosion associated with logging and mining</li>
